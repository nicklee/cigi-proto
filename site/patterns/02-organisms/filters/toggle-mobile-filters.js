import $ from 'jquery';

class ToggleMobileFilters {

    constructor(node) {
        this.$node = $(node);
        this.bindEvents();
    }

    bindEvents() {
        this.$node.on('click', e => {
            e.preventDefault();
            this.$node.parent().toggleClass('active');
        });
    }
}

$('.js-show-menu').each(function() {
    new ToggleMobileFilters(this);
});
